def str_upper(string):
    """
    Функция делает заглавными все буквы каждого слова в строке,
    поступившей на вход
    """
    return string.upper()


def str_title(string):
    """
    Функция делает заглавными первые буквы каждого слова в строке,
    поступившей на вход
    """
    return string.title()
